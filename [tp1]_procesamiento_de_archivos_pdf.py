import tabula
import pandas as pd

# Leer PDF
tablas = tabula.read_pdf("CEPAL147.pdf", pages='23', stream=True)

df = tablas[0]

# 1. Eliminar filas vacías
df = df.dropna(how='all').reset_index(drop=True)

# 2. Renombrar columnas correctamente
df.columns = ["raw", "Media", "Desv", "Min", "25%", "Mediana", "75%", "Max"]

# 3. Separar Variable y n
df[["Variable", "n"]] = df["raw"].str.extract(r"(.+?)\s(\d+)$")

# 4. Eliminar columna original
df.drop(columns=["raw"], inplace=True)

# 5. Unir filas partidas
df["Variable"] = df["Variable"].astype(str).replace('nan', '')

rows_to_drop = []

for i in range(1, len(df)):
    if pd.isna(df.loc[i, "n"]):
        df.loc[i-1, "Variable"] = df.loc[i-1, "Variable"] + " " + str(df.loc[i, "Variable"])
        rows_to_drop.append(i)

df = df.drop(rows_to_drop).reset_index(drop=True)

# 6. Reordenar columnas
df = df[["Variable", "n", "Media", "Desv", "Min", "25%", "Mediana", "75%", "Max"]]

# 7. Limpiar strings numéricos
df = df.replace({",": ".", " ": ""}, regex=True)

print(df.head())

# 8. Guardar
df.to_csv("tabla_limpia.csv", index=False)

