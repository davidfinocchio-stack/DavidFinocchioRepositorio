import spacy
from collections import Counter
import matplotlib.pyplot as plt

# Cargar modelo
nlp = spacy.load("es_core_news_sm")

# Procesar texto
doc = nlp(texto_nlp)

# Limpiar tokens
tokens = [t.lemma_.lower() for t in doc if not t.is_stop and t.is_alpha]

# Contar frecuencias directamente
freq = Counter(tokens)

# Obtener las 15 palabras más frecuentes
top = freq.most_common(15)

# Separar palabras y frecuencias
words = [w for w, f in top]
values = [f for w, f in top]

# Graficar
plt.figure(figsize=(8,5))
plt.bar(words, values)
plt.xticks(rotation=45)
plt.title("15 palabras más frecuentes")
plt.xlabel("Palabras")
plt.ylabel("Frecuencia")
plt.tight_layout()
plt.show()

